def m_9_2_2(data: str):
    resources = [res.strip() for res in data.split(";")]
    uniq_res = sorted(set(resources), key=lambda r: (len(r), r))
    result = []
    result.append(f"–û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Ä–µ—Å—É—Ä—Å–æ–≤: {len(uniq_res)}")
    result.append(f"–°–∞–º—ã–π —Ü–µ–Ω–Ω—ã–π —Ä–µ—Å—É—Ä—Å: {max(uniq_res, key=len)}")
    result.append(f"–°–∞–º—ã–π —Ä–µ–¥–∫–∏–π —Ä–µ—Å—É—Ä—Å: {min(uniq_res, key=len)}")
    result.append(f"–°—É–º–º–∞—Ä–Ω–∞—è —Ü–µ–Ω–Ω–æ—Å—Ç—å —Ä–µ—Å—É—Ä—Å–æ–≤: {sum(map(len, uniq_res))}")
    result.append(f"–†–µ—Å—É—Ä—Å—ã –≤ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ: {', '.join(uniq_res)}")
    return "\n".join(result)


