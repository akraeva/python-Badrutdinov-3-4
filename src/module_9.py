def m_9_1_1(data: str):
    nums = set(map(float, data.split()))
    result = min(nums) + max(nums)
    return f"–°—É–º–º–∞ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–≥–æ –∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —á–∏—Å–ª–∞: {round(result, 2)}"


