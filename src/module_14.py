# import json
# from sys import stdin


def m_14_3_3(data: str):
    d = data.strip().split("\n")
    report = {
        "mission": d[0],
        "success": d[1] == "True",
        "samples": list(map(str.strip, d[2].split(","))),
        "duration": int(d[3]),
    }
    result = json.dumps(report, indent=4)
    with open("report.json", "w", encoding="UTF8") as file:
        json.dump(report, file, indent=4)
    return result


